class titik:
    def __init__(self,a,b):
        self.a = a
        self.b = b
titik_a = titik(3,6)
titik_b = titik(7,2)
titik_x = titik(2,8)
titik_y = titik(9,2)
print('titik A memiliki koordinat ({},{})'.format(titik_a.a,titik_a.b))
print('titik B memiliki koordinat ({},{})'.format(titik_b.a,titik_b.b))
print('titik X memiliki koordinat ({},{})'.format(titik_x.a,titik_x.b))
print('titik Y memiliki koordinat ({},{})'.format(titik_y.a,titik_y.b))
   
